/**
 * Save transmission data to JSON file
 */
function saveTransmissionToFile() {
    // First sync the table to car
    syncTableToGearbox();

    const car = getCurrentCar();
    const numGears = car.gearbox.ratios.length;

    const transmissionData = {
        gearbox: {
            ratios: [...car.gearbox.ratios],
            names: car.gearbox.names.slice(0, numGears),
            finalDrives: [...car.gearbox.finalDrives],
            usePreviousFinalDrive: [...car.gearbox.usePreviousFinalDrive]
        },
        // Legacy support
        differentialRatio: car.gearbox.finalDrives[0] || car.differentialRatio
    };

    // Generate filename
    const filename = `transmission_${numGears}speed`;

    saveToJsonFile(transmissionData, filename);
}

/**
 * Load transmission data from JSON file
 */
function loadTransmissionFromFile() {
    loadFromJsonFile((data) => {
        // Validate the data
        const validation = validateTransmissionData(data);

        if (!validation.valid) {
            alert(`Invalid transmission file:\n${validation.errors.join('\n')}`);
            return;
        }

        // Update the car with new transmission data
        const car = getCurrentCar();
        const numGears = data.gearbox.ratios.length;
        const defaultFinalDrive = data.differentialRatio || 3.73;

        car.gearbox = {
            ratios: [...data.gearbox.ratios],
            names: data.gearbox.names || data.gearbox.ratios.map((_, i) => `${i + 1}${getOrdinalSuffix(i + 1)}`),
            finalDrives: data.gearbox.finalDrives || new Array(numGears).fill(defaultFinalDrive),
            usePreviousFinalDrive: data.gearbox.usePreviousFinalDrive || [false, ...new Array(numGears - 1).fill(true)]
        };

        // Update legacy differential ratio
        car.differentialRatio = car.gearbox.finalDrives[0] || defaultFinalDrive;

        // Re-render the tab to show new values
        const container = document.getElementById('transmission');
        if (container) {
            renderTransmissionTab(container);
        }

        alert(`Transmission loaded: ${numGears}-speed gearbox`);
    });
}
