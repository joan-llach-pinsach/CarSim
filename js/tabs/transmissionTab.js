/**
 * Populate the gear speeds table
 */
function populateGearSpeedsTable() {
    const car = getCurrentCar();
    const speedsBody = document.getElementById('gearSpeedsBody');

    // Find peak torque and power RPMs
    const peakTorqueRpm = findPeakTorqueRpm(car.engineCurve);
    const peakPowerRpm = findPeakPowerRpm(car.engineCurve);

    speedsBody.innerHTML = car.gearbox.ratios.map((ratio, index) => {
        const finalDrive = car.getFinalDrive(index);
        const totalRatio = ratio * finalDrive;
        const speedAtPeakTorque = calculateSpeedAtRpm(peakTorqueRpm, totalRatio, car.wheelRadius);
        const speedAtPeakPower = calculateSpeedAtRpm(peakPowerRpm, totalRatio, car.wheelRadius);
        const optimalShiftSpeed = calculateOptimalShiftSpeed(car, index);
        const speedAtRedline = calculateSpeedAtRpm(car.redlineRpm, totalRatio, car.wheelRadius);
        const speedAtMaxRevs = calculateSpeedAtRpm(car.maxRpm, totalRatio, car.wheelRadius);

        return `
            <tr>
                <td>${car.gearbox.names[index] || `Gear ${index + 1}`}</td>
                <td>${speedAtPeakTorque.toFixed(1)} km/h</td>
                <td>${speedAtPeakPower.toFixed(1)} km/h</td>
                <td>${optimalShiftSpeed !== null ? optimalShiftSpeed.toFixed(1) + ' km/h' : 'N/A'}</td>
                <td>${speedAtRedline.toFixed(1)} km/h</td>
                <td>${speedAtMaxRevs.toFixed(1)} km/h</td>
            </tr>
        `;
    }).join('');
}

/**
 * Calculate speed at given RPM
 * @param {number} rpm - Engine RPM
 * @param {number} totalRatio - Total gear ratio
 * @param {number} wheelRadius - Wheel radius in meters
 * @returns {number} Speed in km/h
 */
function calculateSpeedAtRpm(rpm, totalRatio, wheelRadius) {
    const angularVelocity = rpm * (2 * Math.PI / 60);
    const wheelAngularVelocity = angularVelocity / totalRatio;
    const speedMs = wheelAngularVelocity * wheelRadius;
    return msToKmh(speedMs);
}

/**
 * Create the gear speed range chart
 */
function createGearSpeedChart() {
    const car = getCurrentCar();
    const ctx = document.getElementById('gearSpeedChart')?.getContext('2d');

    if (!ctx) return;

    if (gearSpeedChart) {
        gearSpeedChart.destroy();
    }

    const colors = ['#ef4444', '#f59e0b', '#22c55e', '#3b82f6', '#8b5cf6', '#ec4899', '#06b6d4', '#84cc16'];

    const datasets = car.gearbox.ratios.map((_, index) => {
        const totalRatio = car.getTotalRatio(index);
        const minSpeed = calculateSpeedAtRpm(car.idleRpm, totalRatio, car.wheelRadius);
        const maxSpeed = calculateSpeedAtRpm(car.maxRpm, totalRatio, car.wheelRadius);

        return {
            label: car.gearbox.names[index] || `Gear ${index + 1}`,
            data: [[minSpeed, maxSpeed]],
            backgroundColor: colors[index % colors.length],
            barPercentage: 0.8
        };
    });

    gearSpeedChart = new Chart(ctx, {
        type: 'bar',
        data: {
            labels: ['Speed Range'],
            datasets: datasets
        },
        options: {
            indexAxis: 'y',
            responsive: true,
            maintainAspectRatio: false,
            scales: {
                x: {
                    title: {
                        display: true,
                        text: 'Speed (km/h)'
                    },
                    min: 0
                }
            },
            plugins: {
                legend: {
                    position: 'top'
                },
                tooltip: {
                    callbacks: {
                        label: function(context) {
                            const data = context.raw;
                            return `${context.dataset.label}: ${data[0].toFixed(1)} - ${data[1].toFixed(1)} km/h`;
                        }
                    }
                }
            }
        }
    });
}

/**
 * Create the tractive force vs speed chart
 */
function createTractiveForceChart() {
    const car = getCurrentCar();
    const ctx = document.getElementById('transmissionTractiveForceChart')?.getContext('2d');

    if (!ctx) return;

    if (tractiveForceChart) {
        tractiveForceChart.destroy();
    }

    const colors = ['#ef4444', '#f59e0b', '#22c55e', '#3b82f6', '#8b5cf6', '#ec4899', '#06b6d4', '#84cc16'];
    const numPointsPerGear = 30;

    // Generate data for each gear across its full speed range (idle to max revs)
    const datasets = car.gearbox.ratios.map((_, gearIndex) => {
        const totalRatio = car.getTotalRatio(gearIndex);
        const minSpeed = calculateSpeedAtRpm(car.idleRpm, totalRatio, car.wheelRadius);
        const maxSpeed = calculateSpeedAtRpm(car.maxRpm, totalRatio, car.wheelRadius);
        const speedStep = 1; //(maxSpeed - minSpeed) / numPointsPerGear;

        const dataPoints = [];

        // Add first point at exactly minSpeed
        const minVelocityMs = kmhToMs(minSpeed);
        const minRpm = car.idleRpm;
        const minEngineTorque = interpolateTorque(car.engineCurve, minRpm);
        const minWheelTorque = calculateWheelTorque(minEngineTorque, totalRatio, car.drivetrainEfficiency);
        const minTractiveForce = calculateTractiveForce(minWheelTorque, car.wheelRadius);

        dataPoints.push({
            x: minSpeed,
            y: minTractiveForce,
            rpm: minRpm
        });

        // Loop up to (but not including) maxSpeed
        for (let speed = Math.floor(minSpeed) + 1; speed < maxSpeed; speed += speedStep) {
            const velocityMs = kmhToMs(speed);
            const rpm = calculateEngineRpm(velocityMs, car.wheelRadius, totalRatio);

            // Get engine torque at this RPM
            const engineTorque = interpolateTorque(car.engineCurve, rpm);

            // Calculate wheel torque and tractive force
            const wheelTorque = calculateWheelTorque(engineTorque, totalRatio, car.drivetrainEfficiency);
            const tractiveForce = calculateTractiveForce(wheelTorque, car.wheelRadius);

            dataPoints.push({
                x: speed,
                y: tractiveForce,
                rpm: rpm
            });
        }

        // Add final point at exactly maxSpeed to ensure full RPM range coverage
        const maxVelocityMs = kmhToMs(maxSpeed);
        const maxRpm = car.maxRpm;
        const maxEngineTorque = interpolateTorque(car.engineCurve, maxRpm);
        const maxWheelTorque = calculateWheelTorque(maxEngineTorque, totalRatio, car.drivetrainEfficiency);
        const maxTractiveForce = calculateTractiveForce(maxWheelTorque, car.wheelRadius);

        dataPoints.push({
            x: maxSpeed,
            y: maxTractiveForce,
            rpm: maxRpm
        });

        return {
            label: car.gearbox.names[gearIndex] || `Gear ${gearIndex + 1}`,
            data: dataPoints,
            borderColor: colors[gearIndex % colors.length],
            backgroundColor: colors[gearIndex % colors.length] + '20',
            fill: false,
            tension: 0.3,
            pointRadius: 0,
            borderWidth: 2
        };
    });

    tractiveForceChart = new Chart(ctx, {
        type: 'line',
        data: { datasets },
        options: {
            responsive: true,
            maintainAspectRatio: false,
            interaction: {
                mode: 'nearest',
                axis: 'x',
                intersect: false
            },
            scales: {
                x: {
                    type: 'linear',
                    title: {
                        display: true,
                        text: 'Speed (km/h)'
                    },
                    min: 0
                },
                y: {
                    title: {
                        display: true,
                        text: 'Tractive Force (N)'
                    },
                    min: 0
                }
            },
            plugins: {
                legend: {
                    position: 'top'
                },
                tooltip: {
                    callbacks: {
                        title: function(tooltipItems) {
                            if (tooltipItems.length > 0) {
                                return `Speed: ${tooltipItems[0].parsed.x.toFixed(1)} km/h`;
                            }
                            return '';
                        },
                        label: function(context) {
                            const dataPoint = context.dataset.data[context.dataIndex];
                            return `${context.dataset.label}: ${context.parsed.y.toFixed(0)} N @ ${dataPoint.rpm.toFixed(0)} RPM`;
                        }
                    }
                }
            }
        }
    });
}

/**
 * Setup event listeners for the transmission tab
 */
function setupTransmissionEventListeners() {
    // Gear count change
    document.getElementById('gearCount')?.addEventListener('change', (e) => {
        const newCount = parseInt(e.target.value);
        updateGearCount(newCount);
    });

    // Apply button
    document.getElementById('applyTransmissionBtn')?.addEventListener('click', applyTransmissionChanges);

    // m:n ratio inputs - update decimal when m or n changes
    document.querySelectorAll('.ratio-m-input, .ratio-n-input').forEach(input => {
        input.addEventListener('input', handleMNRatioChange);
    });

    // Decimal ratio input - clear m:n when decimal is entered
    document.querySelectorAll('.gear-ratio-input').forEach(input => {
        input.addEventListener('input', handleDecimalRatioChange);
        input.addEventListener('change', updateGearRatiosDisplay);
    });

    // Final drive inputs - need to cascade to dependent gears
    document.querySelectorAll('.final-drive-input').forEach(input => {
        input.addEventListener('change', updateAllFinalDrivesAndTotals);
    });

    // "Same as previous" checkboxes
    document.querySelectorAll('.use-prev-final-drive').forEach(checkbox => {
        checkbox.addEventListener('change', handleUsePreviousFinalDriveChange);
    });

    // Save/Load buttons
    document.getElementById('saveTransmissionBtn')?.addEventListener('click', saveTransmissionToFile);
    document.getElementById('loadTransmissionBtn')?.addEventListener('click', loadTransmissionFromFile);
}

/**
 * Handle m:n ratio input change
 */
function handleMNRatioChange(e) {
    const index = parseInt(e.target.dataset.index);
    const row = e.target.closest('tr');
    const mInput = row.querySelector('.ratio-m-input');
    const nInput = row.querySelector('.ratio-n-input');
    const decimalInput = row.querySelector('.gear-ratio-input');

    const m = parseFloat(mInput.value);
    const n = parseFloat(nInput.value);

    if (!isNaN(m) && !isNaN(n) && n !== 0) {
        const ratio = m / n;
        decimalInput.value = ratio.toFixed(3);
        updateGearRatiosDisplay();
    }
}

/**
 * Handle decimal ratio input change - clear m:n values
 */
function handleDecimalRatioChange(e) {
    const row = e.target.closest('tr');
    const mInput = row.querySelector('.ratio-m-input');
    const nInput = row.querySelector('.ratio-n-input');

    // Clear m:n inputs when user types in decimal
    mInput.value = '';
    nInput.value = '';
}

/**
 * Handle "use previous final drive" checkbox change
 */
function handleUsePreviousFinalDriveChange(e) {
    const index = parseInt(e.target.dataset.index);
    const row = e.target.closest('tr');
    const finalDriveInput = row.querySelector('.final-drive-input');

    if (e.target.checked) {
        // Disable input and show previous gear's final drive
        finalDriveInput.disabled = true;
        const prevFinalDrive = getEffectiveFinalDrive(index - 1);
        finalDriveInput.value = prevFinalDrive.toFixed(2);
    } else {
        // Enable input
        finalDriveInput.disabled = false;
    }

    // Update all rows from this index onwards (cascade effect)
    updateAllFinalDrivesAndTotals();
}

/**
 * Update all final drive displays and total ratios for all gears
 */
function updateAllFinalDrivesAndTotals() {
    const car = getCurrentCar();
    const rows = document.querySelectorAll('#gearRatiosBody tr');

    rows.forEach((row, index) => {
        const finalDriveInput = row.querySelector('.final-drive-input');
        const usePrevCheckbox = row.querySelector('.use-prev-final-drive');
        const ratioInput = row.querySelector('.gear-ratio-input');
        const totalRatioCell = row.querySelector('.total-ratio-cell');
        const speedCell = row.querySelector('.speed-cell');

        if (!ratioInput || !finalDriveInput) return;

        const ratio = parseFloat(ratioInput.value) || car.gearbox.ratios[index];
        const finalDrive = getEffectiveFinalDrive(index);
        const totalRatio = ratio * finalDrive;
        const speedAtRedline = calculateSpeedAtRpm(car.redlineRpm, totalRatio, car.wheelRadius);

        // Update final drive display if using previous
        if (usePrevCheckbox && usePrevCheckbox.checked) {
            finalDriveInput.value = finalDrive.toFixed(2);
        }

        // Update total ratio and speed
        if (totalRatioCell) totalRatioCell.textContent = totalRatio.toFixed(2);
        if (speedCell) speedCell.textContent = speedAtRedline.toFixed(1) + ' km/h';
    });
}

/**
 * Sync table inputs to car gearbox data
 */
function syncTableToGearbox() {
    const car = getCurrentCar();

    // Sync gear ratios from table inputs
    document.querySelectorAll('.gear-ratio-input').forEach(input => {
        const index = parseInt(input.dataset.index);
        if (index < car.gearbox.ratios.length) {
            car.gearbox.ratios[index] = parseFloat(input.value) || car.gearbox.ratios[index];
        }
    });

    // Sync final drives and use previous flags
    document.querySelectorAll('.final-drive-input').forEach(input => {
        const index = parseInt(input.dataset.index);
        if (index < car.gearbox.finalDrives.length) {
            car.gearbox.finalDrives[index] = parseFloat(input.value) || car.gearbox.finalDrives[index];
        }
    });

    document.querySelectorAll('.use-prev-final-drive').forEach(checkbox => {
        const index = parseInt(checkbox.dataset.index);
        if (index < car.gearbox.usePreviousFinalDrive.length) {
            car.gearbox.usePreviousFinalDrive[index] = checkbox.checked;
        }
    });

    // Update legacy differentialRatio to first gear's final drive
    car.differentialRatio = car.gearbox.finalDrives[0] || car.differentialRatio;
}

/**
 * Update gear count
 * @param {number} newCount - New number of gears
 */
function updateGearCount(newCount) {
    // First sync any edits from the table
    syncTableToGearbox();

    const car = getCurrentCar();
    const currentCount = car.gearbox.ratios.length;

    if (newCount > currentCount) {
        // Add gears with decreasing ratios
        for (let i = currentCount; i < newCount; i++) {
            const lastRatio = car.gearbox.ratios[car.gearbox.ratios.length - 1];
            const lastFinalDrive = car.gearbox.finalDrives[car.gearbox.finalDrives.length - 1];
            car.gearbox.ratios.push(Math.max(0.5, lastRatio * 0.8));
            car.gearbox.names.push(`${i + 1}${getOrdinalSuffix(i + 1)}`);
            car.gearbox.finalDrives.push(lastFinalDrive);
            car.gearbox.usePreviousFinalDrive.push(true);
        }
    } else if (newCount < currentCount) {
        // Remove gears
        car.gearbox.ratios = car.gearbox.ratios.slice(0, newCount);
        car.gearbox.names = car.gearbox.names.slice(0, newCount);
        car.gearbox.finalDrives = car.gearbox.finalDrives.slice(0, newCount);
        car.gearbox.usePreviousFinalDrive = car.gearbox.usePreviousFinalDrive.slice(0, newCount);
    }

    populateGearRatiosTable();
    createGearSpeedChart();
    createTractiveForceChart();
    setupTransmissionEventListeners();
}

/**
 * Get ordinal suffix (st, nd, rd, th)
 * @param {number} n - Number
 * @returns {string} Ordinal suffix
 */
function getOrdinalSuffix(n) {
    const s = ["th", "st", "nd", "rd"];
    const v = n % 100;
    return s[(v - 20) % 10] || s[v] || s[0];
}

/**
 * Get effective final drive for a gear index based on table inputs
 * Traces back through the chain of "same as previous" to find the source value
 * @param {number} index - Gear index
 * @returns {number} Final drive ratio
 */
function getEffectiveFinalDrive(index) {
    // Find the source gear - the first one in the chain without "same as previous" checked
    let sourceIndex = index;

    while (sourceIndex > 0) {
        const row = document.querySelector(`tr[data-index="${sourceIndex}"]`);
        if (!row) break;

        const usePrevCheckbox = row.querySelector('.use-prev-final-drive');
        if (!usePrevCheckbox || !usePrevCheckbox.checked) {
            break; // This gear has its own final drive value
        }
        sourceIndex--;
    }

    // Get the value from the source gear
    const sourceRow = document.querySelector(`tr[data-index="${sourceIndex}"]`);
    if (!sourceRow) return 3.73;

    const finalDriveInput = sourceRow.querySelector('.final-drive-input');
    return parseFloat(finalDriveInput?.value) || 3.73;
}

